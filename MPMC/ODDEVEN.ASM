ASSUME CS:CODE, DS:DATA
DATA SEGMENT
    INPUT_NUM DW 0002H
    MSG1 DB 'EVEN NUMBER', '$'
    MSG2 DB 'ODD NUMBER', '$'
DATA ENDS

CODE SEGMENT

START:
    MOV AX, DATA
    MOV DS, AX

    MOV AX, INPUT_NUM

    RCR AX, 1
    JNC EVEN
    LEA DX, MSG2
    MOV AH, 09H
    INT 21H
    JMP EXIT
EVEN:
    LEA DX, MSG1
    MOV AH, 09H
    INT 21H

EXIT:
    INT 03H

CODE ENDS
END START